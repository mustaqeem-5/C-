#include <iostream>
using namespace std;
int main() {
    // Basic_Arithmetic OPerators
    int x=10;
    int y=20;
    cout<< x + y <<"\n";
    cout<< x - y <<"\n";
    cout<< x * y <<"\n";
    cout<< x / y <<"\n";
    cout<< x % y <<"\n";

    // Increment Decrement
    cout << ++x <<"\n";
    cout<< --y;
    return 0;
}